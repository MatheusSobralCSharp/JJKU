package net.mcreator.jujutsucraftaddon.item;

import net.mcreator.jujutsucraftaddon.item.renderer.WarstaffItemRenderer;
import net.mcreator.jujutsucraftaddon.procedures.WarstaffRightclickedProcedure;
import net.minecraft.client.renderer.BlockEntityWithoutLevelRenderer;
